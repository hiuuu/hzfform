import 'package:flutter/material.dart';
import 'field_model.dart';
import '../core/enums.dart';

class HZFFormTextModel extends HZFFormFieldModel {
  /// Maximum character length
  final int? maxLength;

  /// Minimum character length for validation
  final int? minLength;

  /// Placeholder text when empty
  final String? hint;

  HZFFormTextModel({
    required String tag,
    this.maxLength,
    this.minLength,
    this.hint,

    // Parent props
    String? title,
    String? errorMessage,
    String? helpMessage,
    Widget? prefixWidget,
    Widget? postfixWidget,
    bool? required,
    bool? showTitle,
    dynamic value,
    RegExp? validateRegEx,
    int? weight,
    FocusNode? focusNode,
    FocusNode? nextFocusNode,
    VoidCallback? onTap,
    HZFFormFieldStatusEnum? status,
    bool? enableReadOnly,
  }) : super(
          tag: tag,
          type: HZFFormFieldTypeEnum.text,
          title: title,
          errorMessage: errorMessage,
          helpMessage: helpMessage,
          prefixWidget: prefixWidget,
          postfixWidget: postfixWidget,
          required: required,
          showTitle: showTitle,
          value: value,
          validateRegEx: validateRegEx ??
              (minLength != null
                  ? RegExp(
                      '^.{$minLength,}${maxLength != null ? '{,$maxLength}' : ''}')
                  : null),
          weight: weight,
          focusNode: focusNode,
          nextFocusNode: nextFocusNode,
          onTap: onTap,
          status: status,
          enableReadOnly: enableReadOnly,
        );
}
